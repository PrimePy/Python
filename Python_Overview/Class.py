class Calc:
    def Add(a,b):
        return a+b
    def Sub(a,b):
        return a-b
    def Mul(a,b):
        return a*b
    def Div(a,b):
        return a/b
    def Mod(a,b):
        return a%b
    def Pow(a,b):
        return a**b
print(Calc.Add(2,3))
print(Calc.Sub(3,2))
print(Calc.Mul(3,2))
print(Calc.Div(25,5))
print(Calc.Mod(3,4))
print(Calc.Pow(3,4))
    
